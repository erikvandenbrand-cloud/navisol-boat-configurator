// Get articles by propulsion compatibility
export function getArticlesForPropulsion(propulsion: 'Electric' | 'Diesel' | 'Hybrid' | 'Outboard', articles: Article[]): Article[] {
  return articles.filter(a => {
    if (propulsion === 'Electric') return a.electric_compatible;
    if (propulsion === 'Diesel') return a.diesel_compatible;
    if (propulsion === 'Hybrid') return a.hybrid_compatible;
    return true; // Outboard - return all
  });
}

// Get articles by boat model
export function getArticlesForBoatModel(model: string, articles: Article[]): Article[] {
  return articles.filter(a => a.boat_model_compat.some(m => m === model));
}

// Get articles by category
export function getArticlesByCategory(category: string, articles: Article[]): Article[] {
  return articles.filter(a => a.category === category);
}
